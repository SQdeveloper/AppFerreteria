    private void inicializar_datos(){
        for(int i=1; i<101; i++){
            cbCantidad.addItem(i + "");
        }
        //Obtenemos y mostramos la fecha
        DateTimeFormatter dtf = DateTimeFormatter.ofPattern("yyyy/MM/dd");
        txtFecha.setText(dtf.format(LocalDateTime.now()));
        //Obtenermos y mostramos el codigo de compra
        codigo_aleatorio();
        //Mostramos los datos en la tabla
        mostrar_tabla();
    }
    public void mostrar_tabla(){
        
        String matris[][] = new String[lista.size()][5];
        
        for(int i=0; i<lista.size(); i++){
            matris[i][0] = lista.get(i).getCodigo();
            matris[i][1] = lista.get(i).getProducto();
            matris[i][2] = lista.get(i).getPrecio();
            matris[i][3] = lista.get(i).getCantidad();
            matris[i][4] = lista.get(i).getFecha();
        }
        
        tblVentas.setModel(new javax.swing.table.DefaultTableModel(
            matris,
            new String [] {
                "Codigo", "Producto", "Precio", "Cantidad", "Fecha"
            }
        ));
    }
    
    public LocalDate convertir_a_LocalDate(Date dateToConvert) {
        return dateToConvert.toInstant()
          .atZone(ZoneId.systemDefault())
          .toLocalDate();
    }
    
    public Date ADate(LocalDate currentLocalDate) {
        // Obteniendo la hora del sistema
	ZoneId systemTimeZone = ZoneId.systemDefault();
	// conviritiendo LocalDateTime a ZonedDateTime con el sistema timezone
	ZonedDateTime zonedDateTime = currentLocalDate.atStartOfDay(systemTimeZone);
        // convirtiendo ZonedDateTime a Date usando Date.from() y ZonedDateTime.toInstant()
	Date utilDate = Date.from(zonedDateTime.toInstant());
                
        return utilDate;
  }
    
