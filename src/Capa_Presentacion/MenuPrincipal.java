    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        if(cbProducto.getSelectedIndex()==0){
            JOptionPane.showMessageDialog(null, "Por favor eliga el producto a comprar.");
        }else{
            Empresa temp = new Empresa((String)cbProducto.getSelectedItem(), txtCodigo.getText(), txtPrecio.getText(),(String)cbCantidad.getSelectedItem() , txtFecha.getText());
            lista.add(temp);
            temp.Guardar_Ventas(lista);
            mostrar_tabla();
            codigo_aleatorio();
            JOptionPane.showMessageDialog(null, "Compra registrada correctamente.");
        }
    }//GEN-LAST:event_btnAgregarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        String respuesta = JOptionPane.showInputDialog("Ingrese el codigo de la compra a eliminar:");
        if(ler.consultar(respuesta, lista)==true){
            in = ler.indice();
            lista.remove(in);
            mostrar_tabla();
            ler.Guardar_Ventas(lista);
        }else{
            JOptionPane.showMessageDialog(null, "El codigo ingresado es incorrecto.", "Problema", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

