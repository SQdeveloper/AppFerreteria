    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        if(passContraseña.getText().equals(passContraseña2.getText())){
            Empresa l = new Empresa((String)cmbNivel.getSelectedItem(),txtUsuario.getText(), passContraseña.getText());
            list.add(l);
            l.guardar_cuentas(list);
            JOptionPane.showMessageDialog(null, "Cuenta registrada correctamente.", "", JOptionPane.INFORMATION_MESSAGE);
        }else{
            JOptionPane.showMessageDialog(null, "Las contraseñas ingresadas no son iguales.", "Problema", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

