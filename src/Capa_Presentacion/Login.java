    private void btnIngresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIngresarActionPerformed
        Empresa empresa = new Empresa(txtId.getText(), txtContraseña.getText());
        int flag = empresa.lectura_login(txtId.getText(), txtContraseña.getText());
        
        if(flag == 1){
            JOptionPane.showMessageDialog(null, "Por favor Ingrese su cuenta en las casillas correspondientes.", "Incorrecto", JOptionPane.WARNING_MESSAGE);
        }else if(flag == 2){
            JOptionPane.showMessageDialog(null, "Error, el id de usuario es incorrecto.", "Incorrecto", JOptionPane.WARNING_MESSAGE);
        }else if(flag == 3){
            JOptionPane.showMessageDialog(null, "Error, la contraseña ingresada es incorrecta.", "Incorrecto", JOptionPane.WARNING_MESSAGE);
        }else if(flag == 4){
            this.setVisible(false);
        }
        else{
            JOptionPane.showMessageDialog(null, "Error, id y contraseña son incorrectas.", "Incorrecto", JOptionPane.WARNING_MESSAGE);
        }
        
    }//GEN-LAST:event_btnIngresarActionPerformed

